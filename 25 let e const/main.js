// let permite alterar o valor, e permite escopo local em blocos, porem alguns navegadores não tem suporte

let x = 5; // mesmo que var x = 5;

console.log(x);

x = 12;

console.log(x);

// const não pode ter seu valor alterado, pode ser utilizado com mesmo nome em diferentes escopos sem gerar erros

const y = 10;

console.log(y);
